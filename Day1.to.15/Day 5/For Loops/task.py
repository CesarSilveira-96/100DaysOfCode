fruits = ["Apple", "Peach", "Pear"]

# For, como usado abaixo, faz com que as instruções seguintes sejam executadas multiplas vezes,
# passando por todos os items da lista
# Nesse caso, cada vez que for executada, um item da lista sera atribuido a 'fruit'.
for fruit in fruits:
    print(fruit)
    print(fruit + " " + "pie")

