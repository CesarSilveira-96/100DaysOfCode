# Subscripting / Indicar qual caracter da string quer imprimir com [#]
print("hello"[0])

# Strings
print("123" + "345")

# Integer = Whole Number (números inteiros)
print (123 + 345)

# Large integers = usar _ para separar centenas de milhares, milhares de milhões (...).
# Não é necessário, mas pode ser usado para facilitar visualização
print(123_456_789)

#Float = Floating number (número quebrado/decimal)
print(3.14159)

# Booleanos
print(True)
print(False)
